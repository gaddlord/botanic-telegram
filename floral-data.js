// Helper function to get translated floral dictionary
function getFloralDictionary(lang = 'EN') {
    const baseDict = FLORAL_DICTIONARY_EN;
    const translations = FLORAL_TRANSLATIONS[lang] || {};
    
    const translatedDict = {};
    for (const key in baseDict) {
        const base = baseDict[key];
        const trans = translations[key] || {};
        translatedDict[key] = {
            name: trans.name || base.name,
            meaning: trans.meaning || base.meaning,
            opposite: trans.opposite || base.opposite,
            color: base.color,
            image: base.image
        };
    }
    return translatedDict;
}

